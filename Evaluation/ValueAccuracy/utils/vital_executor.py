import os
import sys
import json
import tempfile
import subprocess
import logging
from pathlib import Path

logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)

class VitalExecutor:
    """
    Executes Python code generated by the LLM to calculate Ground Truth values
    directly from the raw .vital files.
    """
    def __init__(self):
        # Determine the absolute path to the vital files directory
        self.project_root = Path(__file__).resolve().parent.parent.parent.parent
        self.vital_dir = self.project_root / "IndexingAgent" / "data" / "raw" / "Open_VitalDB_1.0.0" / "vital_files"
        
        if not self.vital_dir.exists():
            logger.warning(f"Vital files directory not found at: {self.vital_dir}")

    def execute_code(self, code: str) -> dict:
        """
        Executes the given Python code in an isolated subprocess.
        The code is expected to print a JSON string containing {"result": <value>} at the end.
        
        Args:
            code (str): The Python code to execute.
            
        Returns:
            dict: {"success": bool, "result": Any, "error": str}
        """
        # Inject the vital_dir path into the code so the LLM doesn't have to guess it
        # We prepend a standard setup block to the LLM's code
        setup_code = f"""
import sys
import json
import pandas as pd
import numpy as np
import vitaldb
from pathlib import Path

VITAL_DIR = Path(r"{self.vital_dir}")

# Helper function to ensure output is captured correctly
def output_result(val):
    # Convert numpy types to native python types for JSON serialization
    if isinstance(val, (np.integer, np.int64, np.int32)):
        val = int(val)
    elif isinstance(val, (np.floating, np.float64, np.float32)):
        val = float(val)
    elif isinstance(val, np.ndarray):
        val = val.tolist()
    elif pd.isna(val):
        val = None
        
    print(json.dumps({{"result": val}}))

"""
        full_code = setup_code + "\n" + code

        # Write code to a temporary file
        with tempfile.NamedTemporaryFile(mode='w', suffix='.py', delete=False, encoding='utf-8') as temp_file:
            temp_file.write(full_code)
            temp_file_path = temp_file.name

        try:
            # Execute the temporary file
            result = subprocess.run(
                [sys.executable, temp_file_path],
                capture_output=True,
                text=True,
                timeout=60  # 60 seconds timeout for reading vital files
            )

            if result.returncode != 0:
                logger.error(f"Code execution failed:\n{result.stderr}")
                return {
                    "success": False,
                    "result": None,
                    "error": result.stderr.strip()
                }

            # Parse the output
            output_str = result.stdout.strip()
            
            # Find the last line that looks like our JSON output
            json_output = None
            for line in reversed(output_str.split('\n')):
                if line.startswith('{"result":'):
                    try:
                        parsed = json.loads(line)
                        json_output = parsed.get("result")
                        break
                    except json.JSONDecodeError:
                        continue
                        
            if json_output is not None or '{"result": null}' in output_str:
                return {
                    "success": True,
                    "result": json_output,
                    "error": None
                }
            else:
                return {
                    "success": False,
                    "result": None,
                    "error": f"Could not find valid JSON output in stdout. Raw stdout: {output_str}"
                }

        except subprocess.TimeoutExpired:
            return {
                "success": False,
                "result": None,
                "error": "Execution timed out after 60 seconds."
            }
        except Exception as e:
            return {
                "success": False,
                "result": None,
                "error": str(e)
            }
        finally:
            # Clean up the temporary file
            if os.path.exists(temp_file_path):
                os.remove(temp_file_path)

if __name__ == "__main__":
    # Simple test
    executor = VitalExecutor()
    
    test_code = """
# Test reading the first vital file (0001.vital)
file_path = VITAL_DIR / "0001.vital"
if file_path.exists():
    vf = vitaldb.VitalFile(str(file_path))
    track_names = vf.get_track_names()
    output_result(len(track_names))
else:
    output_result("File not found")
"""
    print("Testing VitalExecutor...")
    res = executor.execute_code(test_code)
    print(f"Result: {res}")
